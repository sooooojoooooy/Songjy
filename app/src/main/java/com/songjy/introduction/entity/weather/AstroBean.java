public class AstroBean {
    /**
     * mr : 05:35
     * ms : 17:34
     * sr : 06:29
     * ss : 17:30
     */

    private String mr;
    private String ms;
    private String sr;
    private String ss;

    public String getMr() {
        return mr;
    }

    public void setMr(String mr) {
        this.mr = mr;
    }

    public String getMs() {
        return ms;
    }

    public void setMs(String ms) {
        this.ms = ms;
    }

    public String getSr() {
        return sr;
    }

    public void setSr(String sr) {
        this.sr = sr;
    }

    public String getSs() {
        return ss;
    }

    public void setSs(String ss) {
        this.ss = ss;
    }
}
